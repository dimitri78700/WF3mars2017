// Créer une variable de type NUMBER avec la valeur 10

// Opérateur d'affectation : =
var myNumber = 10;
console.log(myNumber);

// Opérateur d'incrémentation : ++
myNumber++;
console.log(myNumber);

// Opérateur de décrementation: --
myNumber--;
console.log(myNumber);

// Opérateur d'addition/concaténation : +
var myAdd = 7 + 5;
console.log(myAdd);

var MyConc = 'Salut ' + 'mon ' + 'pote ';
console.log(MyConc);

// Opérateur de soustraction : -
var mySous = 7 - 5;
console.log(mySous);

// Opérateur de multiplication : *
var myMult = 7*5;
console.log(myMult);

// '''  de division : /
var myDivi = 14 / 3;
console.log(myDivi);


// L'opérateur modulo : %
var myModulo = 12 % 5;
console.log(myModulo);

// Opérateur d'addition et d'affectation : +=
var a = 10;
var b = 5;

// a = a + b;
a += b;
console.log(a);