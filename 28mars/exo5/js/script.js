// Créer une variable de type NUMBER avec la valeur 10

// Opérateur d'affectation : =
var myNumber = 10;
console.log(myNumber);

// Opérateur d'incrémentation : ++
myNumber++;
console.log(myNumber);

// Opérateur de décrementation: --
myNumber--;
console.log(myNumber);

// Opérateur d'addition/concaténation : +
var myAdd = 7 + 5;
console.log(myAdd);

var MyConc = 'Salut ' + 'mon ' + 'pote ';
console.log(MyConc);

// Opérateur de soustraction : -
var mySous = 7 - 5;
console.log(mySous);